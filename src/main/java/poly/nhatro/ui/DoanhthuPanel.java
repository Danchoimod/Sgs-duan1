    public void moveTo(int rowIndex) {
       try {
            int rowCount = tblDoanhThu.getRowCount();
            if (rowCount == 0) {
                return;
            }           
            if (rowIndex < 0) {
                index = rowCount - 1; 
            } else if (rowIndex >= rowCount) {
                index = 0; 
            } else {
                index = rowIndex;
            }
            
            tblDoanhThu.setRowSelectionInterval(index, index);         
            fillForm();
            
        } catch (Exception e) {
            e.printStackTrace();
            XDialog.alert("Lỗi khi di chuyển đến dòng: " + e.getMessage());
        }         
    }
