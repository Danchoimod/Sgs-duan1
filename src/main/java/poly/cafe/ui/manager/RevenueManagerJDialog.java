    @Override
    public void open() {
        this.setLocationRelativeTo(null);
        this.selectTimeRange();
    }

    @Override
    public void selectTimeRange() {
        TimeRange range = TimeRange.today();
        switch (cboTimeRanges.getSelectedIndex()) {
            case 0 -> range = TimeRange.today();
            case 1 -> range = TimeRange.thisWeek();
            case 2 -> range = TimeRange.thisMonth();
            case 3 -> range = TimeRange.thisQuarter();
            case 4 -> range = TimeRange.thisYear();
        }
        txtBegin.setText(XDate.format(range.getBegin(), "dd/MM/yyyy"));
        txtEnd.setText(XDate.format(range.getEnd(), "dd/MM/yyyy"));
        this.fillRevenue();
    }

    @Override
    public void fillRevenue() {
        try {
            Date begin = XDate.parse(txtBegin.getText(), "dd/MM/yyyy");
            Date end = XDate.parse(txtEnd.getText(), "dd/MM/yyyy");

            if (begin.after(end)) {
                JOptionPane.showMessageDialog(this,
                    "Ngày bắt đầu phải trước hoặc bằng ngày kết thúc!",
                    "Lỗi",
                    JOptionPane.ERROR_MESSAGE);
                return;
            }

            switch (tabs.getSelectedIndex()) {
                case 0 -> this.fillRevenueByCategory(begin, end);
                case 1 -> this.fillRevenueByUser(begin, end);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Vui lòng nhập đúng định dạng ngày (dd/MM/yyyy)!",
                "Lỗi",
                JOptionPane.ERROR_MESSAGE);
        }
    }

    private void fillRevenueByCategory(Date begin, Date end) {
        try {
            List<Revenue.ByCategory> items = dao.getByCategory(begin, end);

            if (items == null) {
                JOptionPane.showMessageDialog(this,
                    "Không thể lấy dữ liệu doanh thu!",
                    "Lỗi",
                    JOptionPane.ERROR_MESSAGE);
                return;
            }

            DefaultTableModel model = (DefaultTableModel) tblByCategory.getModel();
            model.setRowCount(0);

            if (items.isEmpty()) {
                JOptionPane.showMessageDialog(this,
                    "Không có dữ liệu doanh thu trong khoảng thời gian đã chọn!",
                    "Thông báo",
                    JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            for (Revenue.ByCategory item : items) {
                Object[] row = {
                    item.getCategory(),
                    String.format("%.1f VNĐ", item.getRevenue()),
                    item.getQuantity(),
                    String.format("%.1f VNĐ", item.getMinPrice()),
                    String.format("%.1f VNĐ", item.getMaxPrice()),
                    String.format("%.1f VNĐ", item.getAvgPrice())
                };
                model.addRow(row);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Lỗi khi lấy dữ liệu doanh thu theo danh mục: " + e.getMessage(),
                "Lỗi",
                JOptionPane.ERROR_MESSAGE);
        }
    }

    private void fillRevenueByUser(Date begin, Date end) {
        try {
            List<Revenue.ByUser> items = dao.getByUser(begin, end);

            if (items == null) {
                JOptionPane.showMessageDialog(this,
                    "Không thể lấy dữ liệu doanh thu!",
                    "Lỗi",
                    JOptionPane.ERROR_MESSAGE);
                return;
            }

            DefaultTableModel model = (DefaultTableModel) tblByUser.getModel();
            model.setRowCount(0);

            if (items.isEmpty()) {
                JOptionPane.showMessageDialog(this,
                    "Không có dữ liệu doanh thu trong khoảng thời gian đã chọn!",
                    "Thông báo",
                    JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            items.forEach(item -> {
                Object[] row = {
                    item.getUser(),
                    String.format("%.1f VNĐ", item.getRevenue()),
                    item.getQuantity(),
                    XDate.format(item.getFirstTime(), "HH:mm:ss dd-MM-yyyy"),
                    XDate.format(item.getLastTime(), "HH:mm:ss dd-MM-yyyy")
                };
                model.addRow(row);
            });
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Lỗi khi lấy dữ liệu doanh thu theo nhân viên: " + e.getMessage(),
                "Lỗi",
                JOptionPane.ERROR_MESSAGE);
        }
    }
