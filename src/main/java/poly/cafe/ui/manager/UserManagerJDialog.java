    @Override
    public void open() {
        this.setLocationRelativeTo(null);
        this.fillToTable();
        this.clear();
    }

    @Override
    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblCategories.getModel();
        model.setRowCount(0);
        items = dao.findAll();
        items.forEach(item -> {
            String role = item.isManager() ? "Quản lý" : "Nhân viên";
            String status = item.isEnabled() ? "Hoạt động" : "Tạm ngưng";
            Object[] rowData = {
                item.getUsername(),
                item.getPassword(),
                item.getFullname(),
                item.getPhoto(),
                role,
                status,
                false
            };
            model.addRow(rowData);
        });
    }

    @Override
    public void edit() {
        User entity = items.get(tblCategories.getSelectedRow());
        this.setForm(entity);
        this.setEditable(true);
        tabs.setSelectedIndex(1);
    }

    @Override
    public void create() {
        User entity = this.getForm();
        if (entity != null) {
            try {
                // Kiểm tra trùng tên đăng nhập
                if (dao.findById(entity.getUsername()) != null) {
                    XDialog.alert("Tên đăng nhập đã tồn tại! Vui lòng chọn tên khác.");
                    txtUsername.requestFocus();
                    return;
                }
                
                dao.create(entity);
                this.fillToTable();
                this.clear();
                XDialog.alert("Thêm mới nhân viên thành công!");
            } catch (Exception e) {
                XDialog.alert("Thêm mới nhân viên thất bại: " + e.getMessage());
            }
        }
    }

    @Override
    public void update() {
        User entity = this.getForm();
        dao.update(entity);
        this.fillToTable();
        XDialog.alert("Cập nhật thành công!");
        tabs.setSelectedIndex(0); // Chuyển về tab danh sách
    }

    @Override
    public void delete() {
        if (XDialog.confirm("Bạn thực sự muốn xóa?")) {
            String id = txtUsername.getText();
            dao.DeleteById(id);
            this.fillToTable();
            this.clear();
        }
    }

