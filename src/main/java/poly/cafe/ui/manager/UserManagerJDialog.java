    public void moveLast() {
        this.moveTo(tblCategories.getRowCount() - 1);
    }

    @Override
    public void moveTo(int rowIndex) {
        if (rowIndex < 0) {
            this.moveLast();
        } else if (rowIndex >= tblCategories.getRowCount()) {
            this.moveFirst();
        } else {
            tblCategories.clearSelection();
            tblCategories.setRowSelectionInterval(rowIndex, rowIndex);
            this.edit();
        }
    }

    @Override
    public void setForm(User Entity) {
        txtUsername.setText(Entity.getUsername());
        txtFullName.setText(Entity.getFullname());
        txtPassword.setText(Entity.getPassword());
        txtComfirmPassword.setText(Entity.getPassword());
        XIcon.setIcon(lblPhoto, "photos/" + Entity.getPhoto());
        txtImgAdress.setText(Entity.getPhoto());
        
        // Cập nhật checkbox vai trò
        if (Entity.isManager()) {
            cbManager.setSelected(true);
            cbStaff.setSelected(false);
        } else {
            cbManager.setSelected(false);
            cbStaff.setSelected(true);
        }
        
        // Cập nhật checkbox trạng thái
        if (Entity.isEnabled()) {
            cbRunning.setSelected(true);
            cbPause.setSelected(false);
        } else {
            cbRunning.setSelected(false);
            cbPause.setSelected(true);
        }
    }

    @Override
    public User getForm() {
        if (!validateForm()) {
            return null;
        }

        User entity = new User();
        entity.setFullname(txtFullName.getText().trim());
        entity.setPhoto(imageFileName != null ? imageFileName : txtImgAdress.getText());
        entity.setPassword(txtPassword.getText().trim());
        entity.setUsername(txtUsername.getText().trim());
        
        if (cbManager.isSelected()) {
            entity.setManager(true);
        } else if (cbStaff.isSelected()) {
            entity.setManager(false);
        }
        
        if (cbRunning.isSelected()) {
            entity.setEnabled(true);
        } else if (cbPause.isSelected()) {
            entity.setEnabled(false);
        }

        return entity;
    }

    private boolean validateForm() {
        // Kiểm tra tên đăng nhập
        String username = txtUsername.getText().trim();
        if (username.isEmpty()) {
            XDialog.alert("Vui lòng nhập tên đăng nhập!");
            txtUsername.requestFocus();
            return false;
        }

        // Kiểm tra họ và tên
        String fullname = txtFullName.getText().trim();
        if (fullname.isEmpty()) {
            XDialog.alert("Vui lòng nhập họ và tên!");
            txtFullName.requestFocus();
            return false;
        }

        // Kiểm tra mật khẩu
        String password = txtPassword.getText().trim();
        if (password.isEmpty()) {
            XDialog.alert("Vui lòng nhập mật khẩu!");
            txtPassword.requestFocus();
            return false;
        }

        // Kiểm tra xác nhận mật khẩu
        String confirmPassword = txtComfirmPassword.getText().trim();
        if (!password.equals(confirmPassword)) {
            XDialog.alert("Mật khẩu xác nhận không khớp!");
            txtComfirmPassword.requestFocus();
            return false;
        }

        // Kiểm tra vai trò
        if (!cbManager.isSelected() && !cbStaff.isSelected()) {
            XDialog.alert("Vui lòng chọn vai trò!");
            return false;
        }

        // Kiểm tra trạng thái
        if (!cbRunning.isSelected() && !cbPause.isSelected()) {
            XDialog.alert("Vui lòng chọn trạng thái!");
            return false;
        }

        return true;
    }
