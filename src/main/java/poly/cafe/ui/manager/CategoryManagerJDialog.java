    @Override
    public void open() {
        this.setLocationRelativeTo(null);
        this.fillToTable();
        this.clear();
    }

    @Override
    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblCategories.getModel();
        model.setRowCount(0);
        items = dao.findAll();
        items.forEach(item -> {
            Object[] rowData = {
                item.getId(),
                item.getName(),
                false
            };
            model.addRow(rowData);
        });
    }

    @Override
    public void edit() {
        Category entity = items.get(tblCategories.getSelectedRow());
        this.setForm(entity);
        this.setEditable(true);
        tabs.setSelectedIndex(1);
    }

    @Override
    public void checkAll() {
        this.setCheckedAll(true);
    }

    @Override
    public void uncheckAll() {
        this.setCheckedAll(false);
    }

    private void setCheckedAll(boolean checked) {
        for (int i = 0; i < tblCategories.getRowCount(); i++) {
            tblCategories.setValueAt(checked, i, 2);
        }
    }

    @Override
    public void deleteCheckedItems() {
        if (XDialog.confirm("Bạn có chắc chắn muốn xóa các loại đồ uống đã chọn không?")) {
            boolean hasDeleted = false;
            for (int i = tblCategories.getRowCount() - 1; i >= 0; i--) {
                if ((Boolean) tblCategories.getValueAt(i, 2)) {
                    try {
                        dao.deleteById(items.get(i).getId());
                        hasDeleted = true;
                    } catch (Exception e) {
                        XDialog.alert("Xóa loại đồ uống thất bại: " + e.getMessage());
                    }
                }
            }
            if (hasDeleted) {
                this.fillToTable();
                XDialog.alert("Xóa các loại đồ uống đã chọn thành công!");
            } else {
                XDialog.alert("Vui lòng chọn ít nhất một loại đồ uống để xóa!");
            }
        }
    }

