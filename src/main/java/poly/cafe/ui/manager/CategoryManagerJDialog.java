    @Override
    public void create() {
        Category entity = this.getForm();
        // Kiểm tra để trống
        if (entity.getId() == null || entity.getId().trim().isEmpty()) {
            XDialog.alert("Mã loại không được để trống!");
            return;
        }
        if (entity.getName() == null || entity.getName().trim().isEmpty()) {
            XDialog.alert("Tên loại không được để trống!");
            return;
        }
        // Kiểm tra trùng tên loại
        List<Category> allCategories = dao.findAll();
        for (Category c : allCategories) {
            if (c.getName().equalsIgnoreCase(entity.getName())) {
                XDialog.alert("Tên loại đồ uống đã tồn tại! Vui lòng chọn tên khác.");
                return;
            }
        }
        try {
            // Check for duplicate ID
            if (dao.findById(entity.getId()) != null) {
                XDialog.alert("Mã loại đồ uống đã tồn tại! Vui lòng chọn mã khác.");
                return;
            }
            dao.create(entity);
            this.fillToTable();
            this.clear();
            XDialog.alert("Thêm mới loại đồ uống thành công!");
        } catch (Exception e) {
            XDialog.alert("Thêm mới loại đồ uống thất bại: " + e.getMessage());
        }
    }

    @Override
    public void update() {
        Category entity = this.getForm();
        // Kiểm tra để trống
        if (entity.getId() == null || entity.getId().trim().isEmpty()) {
            XDialog.alert("Mã loại không được để trống!");
            return;
        }
        if (entity.getName() == null || entity.getName().trim().isEmpty()) {
            XDialog.alert("Tên loại không được để trống!");
            return;
        }
        // Kiểm tra trùng tên loại (trừ chính nó)
        List<Category> allCategories = dao.findAll();
        for (Category c : allCategories) {
            if (c.getName().equalsIgnoreCase(entity.getName()) && !c.getId().equals(entity.getId())) {
                XDialog.alert("Tên loại đồ uống đã tồn tại! Vui lòng chọn tên khác.");
                return;
            }
        }
        try {
            dao.update(entity);
            this.fillToTable();
            XDialog.alert("Cập nhật loại đồ uống thành công!");
        } catch (Exception e) {
            XDialog.alert("Cập nhật loại đồ uống thất bại: " + e.getMessage());
        }
    }

    @Override
    public void delete() {
        if (XDialog.confirm("Bạn có chắc chắn muốn xóa loại đồ uống này không?")) {
            String id = txtId.getText();
            try {
                dao.deleteById(id);
                this.fillToTable();
                this.clear();
                XDialog.alert("Xóa loại đồ uống thành công!");
            } catch (Exception e) {
                XDialog.alert("Xóa loại đồ uống thất bại: " + e.getMessage());
            }
        }
    }

    @Override
    public void clear() {
        this.setForm(new Category());
        this.setEditable(false);
    }

    @Override
    public void setEditable(boolean editable) {
        txtId.setEnabled(!editable);
        btnCreate.setEnabled(!editable);
        btnUpdate.setEnabled(editable);
        btnDelete.setEnabled(editable);
        int rowCount = tblCategories.getRowCount();
        btnMoveFirst.setEnabled(editable && rowCount > 0);
        btnMovePrevious.setEnabled(editable && rowCount > 0);
        btnMoveNext.setEnabled(editable && rowCount > 0);
        btnMoveLast.setEnabled(editable && rowCount > 0);
    }

    @Override
    public void moveFirst() {
        this.moveTo(0);
    }

    @Override
    public void movePrevious() {
        this.moveTo(tblCategories.getSelectedRow() - 1);
    }

    @Override
    public void moveNext() {
        this.moveTo(tblCategories.getSelectedRow() + 1);
    }

    @Override
    public void moveLast() {
        this.moveTo(tblCategories.getRowCount() - 1);
    }

    @Override
    public void moveTo(int index) {
        if (index < 0) {
            this.moveLast();
        } else if (index >= tblCategories.getRowCount()) {
            this.moveFirst();
        } else {
            tblCategories.clearSelection();
            tblCategories.setRowSelectionInterval(index, index);
            this.edit();
        }
    }
    

