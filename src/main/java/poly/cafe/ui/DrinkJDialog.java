@Setter
    Bill bill;
    CategoryDAO categoryDao = new CategoryDAOImpl();
    List<Category> categories = List.of();
    DrinkDAO drinkDao = new DrinkDAOImpl();
    List<Drink> drinks = List.of();
    List<Drink> availableDrinks = new ArrayList<>(); // chỉ chứa đồ uống còn hàng

    @Override
    public void setBill(Bill bill) {
        this.bill = bill;
    }

    @Override
    public void open() {
        this.setLocationRelativeTo(null);
        this.fillCategories();
        this.fillDrinks();
    }

    @Override
    public void fillCategories() {
        categories = categoryDao.findAll();
        DefaultTableModel model = (DefaultTableModel) tblCategories.getModel();
        model.setRowCount(0);
        categories.forEach(d -> model.addRow(new Object[]{d.getName()}));
        tblCategories.setRowSelectionInterval(0, 0);
    }

    @Override
    public void fillDrinks() {
        Category category = categories.get(tblCategories.getSelectedRow());
        drinks = drinkDao.findByCategoryId(category.getId());
        DefaultTableModel model = (DefaultTableModel) tblDrinks.getModel();
        model.setRowCount(0);
        availableDrinks.clear();
        drinks.forEach(d -> {
            if (d.isAvailable()) {
                Object[] row = {
                    d.getId(),
                    d.getName(),
                    String.format("%.1f VNĐ", d.getUnitPrice()),
                    String.format("%.0f%%", d.getDiscount()),
                    "Còn hàng",
                    false
                };
                model.addRow(row);
                availableDrinks.add(d);
            }
        });
    }

    @Override
    public void addDrinkToBill() {
        String quantity = XDialog.prompt("Số lượng?");
        if (quantity != null && quantity.length() > 0) {
            if (!quantity.matches("^[0-9]+$")) {
                XDialog.alert("Số lượng phải là số nguyên!");
                return;
            }
            int qty = Integer.parseInt(quantity);
            if (qty <= 0) {
                XDialog.alert("Số lượng phải lớn hơn 0!");
                return;
            }

            Drink drink = availableDrinks.get(tblDrinks.getSelectedRow());
            BillDetailDAOImpl billDetailDao = new BillDetailDAOImpl();
            // Tìm chi tiết hóa đơn đã có với billId và drinkId này chưa
            java.util.List<BillDetail> existingDetails = billDetailDao.findByBillId(bill.getId());
            BillDetail existed = null;
            for (BillDetail d : existingDetails) {
                if (d.getDrinkId().equals(drink.getId())) {
                    existed = d;
                    break;
                }
            }
            if (existed != null) {
                // Đã có, tăng số lượng
                existed.setQuantity(existed.getQuantity() + qty);
                billDetailDao.update(existed);
            } else {
                // Chưa có, tạo mới
                BillDetail detail = new BillDetail();
                detail.setBillId(bill.getId());
                detail.setDiscount(drink.getDiscount());
                detail.setDrinkId(drink.getId());
                detail.setQuantity(qty);
                detail.setUnitPrice(drink.getUnitPrice());
                billDetailDao.create(detail);
            }
        }
    }
