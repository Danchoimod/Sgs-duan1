    public void setTimeLabel(JLabel label) {
        try {
            URL url = new URL("https://timeapi.io/api/Time/current/zone?timeZone=Asia/Ho_Chi_Minh");
            HttpURLConnection conn = (HttpURLConnection) url.openConnection();
            conn.setRequestMethod("GET");

            BufferedReader reader = new BufferedReader(new InputStreamReader(conn.getInputStream()));
            StringBuilder response = new StringBuilder();
            String line;

            while ((line = reader.readLine()) != null) {
                response.append(line);
            }

            reader.close();
            JSONObject json = new JSONObject(response.toString());
            String time = json.getString("time");

            label.setText("Giờ hiện tại: " + time); // Gắn vào JLabel
        } catch (Exception e) {
            label.setText("Không lấy được giờ");
            e.printStackTrace();
        }
    }

    public void startRealtimeClock(JLabel label) {
        Timer timer = new Timer(5000, e -> setTimeLabel(label)); // 5000 ms = 5 giây
        timer.setRepeats(true);
        timer.start();
        setTimeLabel(label); // Gọi lần đầu để hiển thị ngay
    }
