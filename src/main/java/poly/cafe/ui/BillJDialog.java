    private void btnTinhTienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTinhTienActionPerformed
        // TODO add your handling code here:
        try {
            // Lấy số tiền khách gửi
            String khachGuiStr = txtKhachGui.getText().replaceAll("[^\\d.]", "");
            double khachGui = Double.parseDouble(khachGuiStr);

            // Tính tổng thành tiền từ billDetails
            double tongThanhTien = 0;
            for (BillDetail d : billDetails) {
                double amount = d.getUnitPrice() * d.getQuantity() * (1 - d.getDiscount() / 100);
                tongThanhTien += amount;
            }

            // Tính tiền thừa
            double tienThua = khachGui - tongThanhTien;

            // Hiển thị kết quả lên txtThanhTien
            if (tienThua >= 0) {
                txtThanhTien.setText(String.format("%.0f VNĐ", tienThua));
            } else {
                txtThanhTien.setText(String.format("-%.0f VNĐ", -tienThua));
            }
        } catch (NumberFormatException e) {
            txtThanhTien.setText("Nhập số hợp lệ!");
        }
    }//GEN-LAST:event_btnTinhTienActionPerformed

