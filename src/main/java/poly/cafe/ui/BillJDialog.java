    public void fillBillDetails() {
        DefaultTableModel model = (DefaultTableModel) tblBillDetails.getModel();
        model.setRowCount(0);
        billDetails = List.of();

        String idTextValue = txtId.getText();
        if (idTextValue != null && !idTextValue.isBlank() && !"null".equals(idTextValue.trim())) {
            try {
                Long billId = Long.valueOf(idTextValue.trim());
                billDetails = billDetailDao.findByBillId(billId);
            } catch (NumberFormatException e) {
                // Log error or handle silently
                System.out.println("Error parsing bill ID: " + e.getMessage());
            }
        }

        billDetails.forEach(d -> {
            var amount = d.getUnitPrice() * d.getQuantity() * (1 - d.getDiscount() / 100);
            Object[] rowData = {
                d.getDrinkName(),
                String.format("%.1f VNĐ", d.getUnitPrice()),
                String.format("%.0f%%", d.getDiscount()),
                d.getQuantity(),
                String.format("%.1f VNĐ", amount)
            };
            model.addRow(rowData);
        });
    }

